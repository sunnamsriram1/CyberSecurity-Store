# Ruby Hashes
# A Ruby hash is a collection of unique keys and their values. They are similar to arrays but array use integer as an index and hash use any object type.
  # They are also called associative arrays, dictionaries or maps.

# Creating Ruby Hash
  name = {"key1" => "value1", "key2" => "value2", "key3" => "value3"...}


  color = {
    "Rose" => "red",
    "Lily" => "purple",
    "Marigold" => "yellow",
    "Jasmine" => "white"
  }
  puts color['Rose']
  puts color['Lily']
  puts color['Marigold']
  puts color['Jasmine']
