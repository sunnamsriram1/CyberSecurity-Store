# Numbers
# Integer
integer_example = 42

# Float
float_example = 3.14

# Strings
# Single-quoted string
single_quoted_string = 'Hello, world!'

# Double-quoted string
double_quoted_string = "Hello, world!"

# String interpolation
name = "Alice"
interpolated_string = "Hello, #{name}!"

# Symbols
# Symbol
symbol_example = :my_symbol

# Hashes
# Hash with symbol keys
hash_with_symbol_keys = { name: "Alice", age: 30 }

# Hash with string keys
hash_with_string_keys = { "name" => "Alice", "age" => 30 }

# Arrays
# Array of integers
array_of_integers = [1, 2, 3, 4, 5]

# Array of mixed types
array_of_mixed_types = [1, "two", :three, 4.0, [5]]

# Booleans
# Boolean values
true_value = true
false_value = false

# Print all examples to verify
puts "Integer example: #{integer_example}"
puts "Float example: #{float_example}"
puts "Single-quoted string: #{single_quoted_string}"
puts "Double-quoted string: #{double_quoted_string}"
puts "Interpolated string: #{interpolated_string}"
puts "Symbol example: #{symbol_example}"
puts "Hash with symbol keys: #{hash_with_symbol_keys}"
puts "Hash with string keys: #{hash_with_string_keys}"
puts "Array of integers: #{array_of_integers}"
puts "Array of mixed types: #{array_of_mixed_types}"
puts "True value: #{true_value}"
puts "False value: #{false_value}"
