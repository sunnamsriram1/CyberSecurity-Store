// Primitive Data Types
let string = 'hello'; // Represents textual data
console.log("This is a string:", string);

let number = 42; // Represents both integers and floating-point numbers
console.log("This is a number:", 6 + 6);

let bigint = 9007199254740991n; // Represents large integers
console.log("This is a bigint:", bigint);

let boolean = true; // Represents true or false
console.log("This is a boolean:", boolean);

let undefinedValue; // Represents an uninitialized variable
console.log("This is undefined:", undefinedValue);

let nullValue = null; // Represents an empty or non-existent value
console.log("This is null:", nullValue);

let nanValue = NaN; // Represents Not-a-Number
console.log("This is NaN:", nanValue);

let symbol = Symbol('id'); // Represents unique identifiers
console.log("This is a symbol:", symbol);

// Non-Primitive Data Type
let object = { key: 'value' }; // Represents a collection of properties
console.log("This is an object:", object);
